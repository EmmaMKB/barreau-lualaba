                                                                Mes Chers Confrères ;
                                                                L’ordonnance-loi n° 79-028 du 28 septembre 1979, portant
                                                                organisation du barreau, du corps des défenseurs judiciaires
                                                                et du corps des mandataires de l’État définit à son article
                                                                1er la profession d’avocat ainsi : « Les avocats sont des
                                                                auxiliaires de justice chargés d’assister ou représenter les
                                                                parties, postuler, conclure et plaider devant les
                                                                juridictions.
                                                                Ils peuvent consulter, conseiller, concilier, rédiger des
                                                                actes sous seing privé, assister ou représenter les parties
                                                                en dehors des juridictions ».
                                                                L’avocat a toujours été considéré comme le défenseur naturel
                                                                des droits de chacun. L’exercice libéral de sa profession
                                                                fait de lui le garant des libertés individuelles et la
                                                                sentinelle de la démocratie.
                                                                C’est à ce titre qu’il lui est reconnu l’inviolabilité de
                                                                son cabinet et celle de son secret professionnel.
                                                                Néanmoins, il y a lieu de reconnaître que les limites dans
                                                                l’exercice de sa fonction sont posées par les termes de
                                                                l’article 14 de l’ordonnance-loi précitée en ces termes : «
                                                                Je jure de respecter la constitution, d’obéir à la loi,
                                                                d’exercer la défense et le conseil avec dignité, conscience,
                                                                indépendance et humanité, de ne rien dire ou publier de
                                                                contraire aux lois, aux décisions judiciaires , aux bonnes
                                                                mœurs, à la sécurité de l’État et à la paix publique, de ne
                                                                jamais m’écarter du respect dû aux tribunaux, aux magistrats
                                                                et aux autorités publiques, de ne conseiller ou défendre
                                                                aucune cause que je ne croirais juste en mon âme et
                                                                conscience ».
                                                                Il en résulte que la quintessence de ce serment constitue
                                                                l’ossature de toute la déontologie qui s’impose à l’avocat
                                                                en déterminant les règles à observer tant vis- à- vis des
                                                                autorités publiques et des tribunaux qu’à l’égard de ses
                                                                clients.
                                                                La pratique progressive de la profession d’avocat a provoqué
                                                                un changement de paradigmes en ce que l’avocat n’est plus
                                                                resté dans la fonction de la défense devant les tribunaux,
                                                                mais a en revanche intégré les fonctions de conseil et de
                                                                rédacteur d’acte tant pour les particuliers que pour les
                                                                entreprises ainsi que ses interventions dans le domaine de
                                                                la fiscalité dans ses multiples manifestations.
                                                                Au-delà de ses fonctions classiques telles que développées
                                                                ci-dessus, l’avocat se retrouve de plus en plus engagé dans
                                                                les mécanismes alternatifs de résolution des litiges dont
                                                                font partie l’arbitrage et la médiation qui constituent un
                                                                palliatif aux contraintes de la justice étatique en
                                                                déliquescence.
                                                                Dès lors, la justice privée, ici l’arbitrage a donné
                                                                l’opportunité à l’avocat de doubler ses compétences qui font
                                                                de lui non seulement un consultant mais aussi un juge, d’où
                                                                l’extension de son expertise qui constituent une complétude
                                                                dans l’assurance de la défense des intérêts de ses clients.
                                                                Au demeurant, la formation continue de l’avocat est un atout
                                                                majeur qui entre en ligne de compte de sa mission qui lui
                                                                permet ainsi d’éviter au client non seulement de tomber dans
                                                                les pièges, mais aussi et surtout de lui procurer les bonnes
                                                                solutions. Le ministère de l’avocat s’avère ainsi
                                                                indispensable tant dans son assistance dans un procès qu’en
                                                                dehors de celui-ci, dont il est capable de faire éviter.
                                                                Il importe dès lors de recommander la consultation de
                                                                l’avocat pour assurer la défense de ses intérêts tant devant
                                                                les juridictions et les autorités publiques qu’en dehors de
                                                                ces derniers.

